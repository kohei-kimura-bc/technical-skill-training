# 問題2: リストの検索とカウント
print("\n問題2: リストの検索とカウント")
numbers = [10, 20, 30, 20, 40, 50, 20, 60, 70]

# 1. index()メソッドを使って、最初の20の位置（インデックス）を取得してください
index_of_20 = numbers.index(20)

# 2. count()メソッドを使って、20の出現回数をカウントしてください
count_of_20 = numbers.count(20)

# 3. リストを逆順にするメソッドを使用してください (reverse)
numbers.reverse()

# 4. 結果を表示してください
print("20の最初のインデックス:", index_of_20)
print("20の出現回数:", count_of_20)
print("逆順にしたリスト:", numbers)
# 出力:
# 20の最初のインデックス: 1
# 20の出現回数: 3
# 逆順にしたリスト: [70, 60, 20, 50, 40, 20, 30, 20, 10]
