/*
変数を出力することができる
*/
public class Output3{
    public static void main(String[] args) {

        //変数の宣言と代入
        String message = "こんにちは！";
        int num = 123456;

        // 文字型変数のデータを出力
        System.out.println(message);
        //数値型変数のデータを出力
        System.out.println(num);

        //変数と文字列を一緒に出力
        System.out.println(message + "今日も良い天気ですね");
        
    }
}