# 問題：for文を使って1から10までの数値の合計を計算しよう
#
# このプログラムでは、for文を使って1から10までの数値を足し合わせます。
# 合計値を保存する変数を作り、for文で各数値を足していきましょう。
#
# 手順:
# 1. 合計を保存するための変数 total を作成し、0 で初期化してください
# 2. 1から10までの数値をループするfor文を書いてください
# 3. ループの中で、各数値を total に足してください
# 4. 最後に合計値を表示してください

# 1. 合計を保存する変数を初期化
total = 0

# 2. 1から10までのループを書く
for i in range(1, 11):
    # 3. 各数値を合計に足す
    total = total + i
    # または簡略化して: total += i

# 4. 結果を表示
print("1から10までの合計は:", total)

# 出力結果: 1から10までの合計は: 55
